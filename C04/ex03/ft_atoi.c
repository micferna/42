#include <stdio.h>
int		ft_atoi(char *str)
{
	int a;
	int b;
	int i;

	a = 1;
	i = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			a = a * -1;
		i++;
	}
	b = 0;
	while (str[i] >= '0' && str[i] <= '9')
	{
		b = b * 10 + (str[i] - '0');
		i++;
	}
	return (b * a);
}

int main()
{
	printf("%d", ft_atoi("      +-----++-+--45785jh554"));
}
