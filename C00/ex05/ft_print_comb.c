void	print_loose(int i)
{
	if (i != '7')
	{
		write(1, ",", 1);
		write(1, " ", 1);
	}
}

void	ft_print_comb(void)
{
	int i;
	int y;
	int z;

	i = '0';
	y = '1';
	z = '2';
	while (i <= '7')
	{
		y = i + 1;
		while (y <= '8')
		{
			z = y + 1;
			while (z <= '9')
			{
				write(1, &i, 3);
				write(1, &y, 1);
				write(1, &z, 1);
				print_loose(i);
				z++;
			}
			y++;
		}
		i++;
	}
}

