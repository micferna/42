void	ft_div_mod(int a, int b, int *div, int *mod)
{
	*div = a / b;
	*mod = a % b;
}

int	main(void)
{
	int f;
	int g;

	ft_div_mod(5, 2, &f, &g);
	printf("%d\n%d", f, g);
}
